import firebaseConfig from '../firebase-applet-config.json';
import { getAuth } from 'firebase/auth';

enum OperationType {
  CREATE = 'create',
  UPDATE = 'update',
  DELETE = 'delete',
  LIST = 'list',
  GET = 'get',
  WRITE = 'write',
}

interface FirestoreErrorInfo {
  error: string;
  operationType: OperationType;
  path: string | null;
  authInfo: {
    userId?: string | null;
    email?: string | null;
    emailVerified?: boolean | null;
    isAnonymous?: boolean | null;
  }
}

/**
 * Enhanced error handler for Firestore security violations.
 * Blocks unauthorized access and logs metadata for debugging.
 */
export function handleFirestoreError(error: unknown, operationType: OperationType, path: string | null) {
  const auth = getAuth();
  const errInfo: FirestoreErrorInfo = {
    error: error instanceof Error ? error.message : String(error),
    authInfo: {
      userId: auth.currentUser?.uid,
      email: auth.currentUser?.email,
      emailVerified: auth.currentUser?.emailVerified,
      isAnonymous: auth.currentUser?.isAnonymous
    },
    operationType,
    path
  };
  
  // Log the security violation to the server console (monitored)
  console.error('[SECURITY VIOLATION]:', JSON.stringify(errInfo));
  
  // Re-throw a clean error for the UI
  throw new Error('Access Denied: You do not have permission to perform this action.');
}
